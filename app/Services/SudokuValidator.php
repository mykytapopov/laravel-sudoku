<?php

declare(strict_types=1);

namespace App\Services;

class SudokuValidator
{
    /**
     * @param  array  $board
     *
     * @return bool
     */
    public function isValid(array $board): bool
    {
        $dummy = [
            ["5", ".", "4", ".", "7", ".", ".", ".", "."],
            [".", ".", ".", "1", "9", "5", ".", ".", "."],
            [".", "9", "8", ".", ".", ".", ".", "6", "."],
            ["8", ".", ".", ".", "6", ".", ".", ".", "3"],
            ["4", ".", ".", "8", ".", "3", ".", ".", "1"],
            ["7", ".", ".", ".", "2", ".", ".", ".", "6"],
            [".", "6", ".", ".", ".", ".", "2", "8", "."],
            [".", ".", ".", "4", "1", "9", ".", ".", "5"],
            [".", ".", ".", ".", "8", ".", ".", "7", "9"],
        ];

        return $dummy === $board;
    }
}
